// Get icon by key
export function getIcon(key) {
  return PRODUCT_ICONS[key] || PRODUCT_ICONS["other"];
}

// Get all icons as array
export function getAllIcons() {
  return Object.entries(PRODUCT_ICONS).map(([key, val]) => ({ key, ...val }));
}

// Get icons by category
export function getIconsByCategory(categoryId) {
  if (categoryId === "all") return getAllIcons();
  const cat = ICON_CATEGORIES.find(c => c.id === categoryId);
  if (!cat) return getAllIcons();
  return cat.keys.map(key => ({ key, ...PRODUCT_ICONS[key] })).filter(Boolean);
}
